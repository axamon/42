#include <unistd.h>

void	ft_print_comb2(void)
{
	char a,b;
	
	a = '0';
	b = '0';

	int i;
	i = 0;
	while(i < 100)
	{
		write(1, &a, 1);
		write(1, &b, 1);
		write(1, ", ", 2);
		a++;
		b++;
		i++;
	}
}
