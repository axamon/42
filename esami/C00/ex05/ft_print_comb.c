#include <unistd.h>
void update();

void	ft_print_comb(void)
{
	char c[3];
	
	c[0] = '0';
      	c[1] = '1';
      	c[2] = '2';

	while(1)
	{
		if (c[2] > '9')
			update(c, 1);
		if ( c[1] > '8')
			update(c, 2);
		if (c[0] != c[1] && c[1] != c[2] && c[2] != c[0])
		{
			write(1, &c, 3);
			if(c[0] == '7' &&  c[1] == '8' && c[2] == '9')
				break;
			write(1, ", ", 2);
		}
			update(c,0);
	}
}

void	update(char c[], int i)
{
	if (i == 0)
		c[2]++;
	if (i == 1)
	{
		c[1]++;
		c[2] = c[1] + 1;
	}
	if (i == 2)
	{
		c[0]++;
		c[1] = c[0] + 1;
		c[2] = c[1] + 1;
	}
}
